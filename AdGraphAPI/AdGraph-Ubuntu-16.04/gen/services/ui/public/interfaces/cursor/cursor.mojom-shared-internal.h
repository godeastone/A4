#pragma pack(push, 1)
class  CursorData_Data {
 public:
  class BufferWriter {
   public:
    BufferWriter() = default;

    void Allocate(mojo::internal::Buffer* serialization_buffer) {
      serialization_buffer_ = serialization_buffer;
      index_ = serialization_buffer_->Allocate(sizeof(CursorData_Data));
      new (data()) CursorData_Data();
    }

    bool is_null() const { return !serialization_buffer_; }
    CursorData_Data* data() {
      DCHECK(!is_null());
      return serialization_buffer_->Get<CursorData_Data>(index_);
    }
    CursorData_Data* operator->() { return data(); }

   private:
    mojo::internal::Buffer* serialization_buffer_ = nullptr;
    size_t index_ = 0;

    DISALLOW_COPY_AND_ASSIGN(BufferWriter);
  };

  static bool Validate(const void* data,
                       mojo::internal::ValidationContext* validation_context);

  mojo::internal::StructHeader header_;
  int32_t cursor_type;
  float scale_factor;
  mojo::internal::Pointer<::mojo_base::mojom::internal::TimeDelta_Data> frame_delay;
  mojo::internal::Pointer<::gfx::mojom::internal::Point_Data> hotspot_in_pixels;
  mojo::internal::Pointer<mojo::internal::Array_Data<mojo::internal::Pointer<::skia::mojom::internal::Bitmap_Data>>> cursor_frames;

 private:
  CursorData_Data();
  ~CursorData_Data() = delete;
};
